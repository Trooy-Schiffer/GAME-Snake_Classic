package com.thiago_souza.snake.entities;

public class Entity {

	protected double x, y;
	protected int width, height;
	
	public Entity(double x, double y, int width, int height) {
		super();
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	// ------(Sets/Gets)----------------------
	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}
}
